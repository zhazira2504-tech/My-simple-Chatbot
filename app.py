# -*- coding: utf-8 -*-
import streamlit as st

def simple_chatbot(user_input):
  """
  –≠—Ç–∞ —Ñ—É–Ω–∫—Ü–∏—è –æ—Å—Ç–∞–ª–∞—Å—å –ø–æ—á—Ç–∏ –±–µ–∑ –∏–∑–º–µ–Ω–µ–Ω–∏–π.
  –û–Ω–∞ –ø–æ-–ø—Ä–µ–∂–Ω–µ–º—É —è–≤–ª—è–µ—Ç—Å—è "–º–æ–∑–≥–æ–º" –Ω–∞—à–µ–≥–æ –±–æ—Ç–∞.
  """
  user_input = user_input.lower()
  knowledge_base = {
      "–ø—Ä–∏–≤–µ—Ç": "–ó–¥—Ä–∞–≤—Å—Ç–≤—É–π—Ç–µ! –Ø –≤–µ–±-–≤–µ—Ä—Å–∏—è –ø—Ä–æ—Å—Ç–æ–≥–æ —á–∞—Ç-–±–æ—Ç–∞.",
      "–∫–∞–∫ –¥–µ–ª–∞": "–°–ø–∞—Å–∏–±–æ, —É –º–µ–Ω—è –≤—Å–µ –æ—Ç–ª–∏—á–Ω–æ! –Ø —Ä–∞–±–æ—Ç–∞—é –Ω–∞ —Å–µ—Ä–≤–µ—Ä–∞—Ö Streamlit.",
      "–ø–æ–≥–æ–¥–∞": "–Ø –≤—Å–µ –µ—â–µ –Ω–µ —É–º–µ—é –ø—Ä–µ–¥—Å–∫–∞–∑—ã–≤–∞—Ç—å –ø–æ–≥–æ–¥—É.",
      "–∏–º—è": "–ú–æ–∂–µ—Ç–µ –∑–≤–∞—Ç—å –º–µ–Ω—è –í–µ–±-–ë–æ—Ç.",
      "–ø–æ–∫–∞": "–î–æ —Å–≤–∏–¥–∞–Ω–∏—è! –û–±–Ω–æ–≤–∏—Ç–µ —Å—Ç—Ä–∞–Ω–∏—Ü—É, —á—Ç–æ–±—ã –Ω–∞—á–∞—Ç—å –∑–∞–Ω–æ–≤–æ."
  }
  for keyword, response in knowledge_base.items():
    if keyword in user_input:
      return response
  return "–ò–∑–≤–∏–Ω–∏—Ç–µ, —è –≤–∞—Å –Ω–µ –ø–æ–Ω—è–ª. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–µ—Ä–µ—Ñ—Ä–∞–∑–∏—Ä–æ–≤–∞—Ç—å."

# --- –ò–Ω—Ç–µ—Ä—Ñ–µ–π—Å –Ω–∞—à–µ–≥–æ –≤–µ–±-–ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è ---

# –ó–∞–≥–æ–ª–æ–≤–æ–∫ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–π —É–≤–∏–¥—è—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏
st.title("ü§ñ –ú–æ–π –ø–µ—Ä–≤—ã–π –æ–±–ª–∞—á–Ω—ã–π —á–∞—Ç-–±–æ—Ç")

# –°–æ–∑–¥–∞–µ–º –ø–æ–ª–µ –¥–ª—è –≤–≤–æ–¥–∞ —Ç–µ–∫—Å—Ç–∞ —Å –ø–æ–¥—Å–∫–∞–∑–∫–æ–π
user_text = st.text_input("–ù–∞–ø–∏—à–∏—Ç–µ —á—Ç–æ-–Ω–∏–±—É–¥—å –±–æ—Ç—É –∑–¥–µ—Å—å:")

# –ü—Ä–æ–≤–µ—Ä—è–µ–º, –≤–≤–µ–ª –ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∫–∞–∫–æ–π-—Ç–æ —Ç–µ–∫—Å—Ç
if user_text:
  # –ï—Å–ª–∏ —Ç–µ–∫—Å—Ç –µ—Å—Ç—å, –ø–æ–ª—É—á–∞–µ–º –æ—Ç–≤–µ—Ç –æ—Ç –Ω–∞—à–µ–≥–æ –±–æ—Ç–∞
  bot_response = simple_chatbot(user_text)
  # –ò –≤—ã–≤–æ–¥–∏–º –æ—Ç–≤–µ—Ç –Ω–∞ —ç–∫—Ä–∞–Ω
  st.write(f"**–ë–æ—Ç:** {bot_response}")
